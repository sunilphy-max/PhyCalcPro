/**
 * Extract Governing equations sections from docs/modules/*.md into equation registry stubs.
 * Run: node scripts/sync-equation-registry.mjs
 *
 * Output: src/lib/standards/equations/generated.ts (merged at build time via index.ts optional import)
 */
import fs from "node:fs";
import path from "node:path";

const root = process.cwd();
const docsDir = path.join(root, "docs", "modules");
const outFile = path.join(root, "src", "lib", "standards", "equations", "generated.ts");

const HAND_CURATED = new Set(["beams", "columns", "gears", "combined-loading", "welds"]);

function extractEquations(markdown) {
  const match = markdown.match(/\*\*Governing equations\*\*([\s\S]*?)(?:\n\*\*|\n## |\n---|\Z)/);
  if (!match) return [];

  const block = match[1];
  const rawBlocks = [...block.matchAll(/\\\[([\s\S]*?)\\\]/g)];
  return rawBlocks.map((m, i) => ({
    id: `eq-${i + 1}`,
    label: `Equation ${i + 1}`,
    expression: m[1].replace(/\s+/g, " ").trim(),
  }));
}

const lines = [
  "// Auto-generated by scripts/sync-equation-registry.mjs — do not edit by hand.",
  'import type { EquationReference } from "../types";',
  "",
  "export const generatedModuleEquations: Record<string, EquationReference[]> = {",
];

for (const file of fs.readdirSync(docsDir).filter((f) => f.endsWith(".md"))) {
  const moduleId = file.replace(/\.md$/, "");
  if (HAND_CURATED.has(moduleId)) continue;

  const content = fs.readFileSync(path.join(docsDir, file), "utf8");
  const equations = extractEquations(content);
  if (!equations.length) continue;

  lines.push(`  "${moduleId}": [`);
  for (const eq of equations) {
    const expr = eq.expression.replace(/\\/g, "\\\\").replace(/"/g, '\\"');
    lines.push(`    { id: "${eq.id}", label: "${eq.label}", expression: "${expr}" },`);
  }
  lines.push("  ],");
}

lines.push("};", "");

fs.writeFileSync(outFile, lines.join("\n"), "utf8");
console.log(`Wrote ${outFile}`);
